//Bitwize Operator
let x=5;
let y=3;

//Bitwise AND
console.log(x & y)  //0101 & 0011 =0001 Output:1

//Bitwise OR
console.log(x | y) //0101 | 0011=0111 Output: 7

//Bitwise XOR
console.log(x^y)  //0101 ^ 0011=0110  Output: 6

//Bitwise left shift
console.log(x<<2);

//Bitwise right shift
console.log(y>>3);