//1.Arithmetic Operator
let a= 3 + 2;
console.log("a =" + a);  //Output: a = 5

let b=10-7;
console.log ("b = " + b); //Output:b = 3;

//Exponentian Operator
let c = 2 ** 3;
console.log("c ="+c);  //Output:c = 8

//Increment Operator
var d=2;
var d1 = d++;
console.log("d1 = "+d1);  //output: d1 = 2
console.log("d = "+ d);  //Output: d = 3

//Decrement Operator
var d=2;
var d1 = d--;
console.log("d1 = "+d1);  //output: d1 = 2
console.log("d = "+ d);  //Output: d = 1

//Unary plus Operator
let numString = "123";
let num = +numString;
console.log(num + 2); // Output: 125

//Unary negative Operator
let num1 = 5;
let negNum = -num1;
console.log(negNum); // Output: -5

